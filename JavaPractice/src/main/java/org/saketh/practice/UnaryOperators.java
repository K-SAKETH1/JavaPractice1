package org.saketh.practice;

public class UnaryOperators {
	public static void main(String[] args)
	{
		//Pre increment
		int a = 10;
		int b = ++a;
		System.out.println("a: "+a);
		System.out.println("b: "+b);
		//Post increment
		int c = 10;
		int d = c++;
		System.out.println("c: "+c);
		System.out.println("d: "+d);
	}
}
